-- Calculating case rate per 100,000 people to ensure fair anaylsis across counties -- 

SELECT c.FIPS, c.COUNTY_NAME, c.STATE_NAME, t.SCAPULARIS_STATUS,
       (cd.Y2020 + cd.Y2021 + cd.Y2022 + cd.Y2023) AS total_cases,
       p.POP_2023,
       ROUND(((cd.Y2020 + cd.Y2021 + cd.Y2022 + cd.Y2023) / p.POP_2023) * 100000, 2) AS case_rate_per_100k
FROM COUNTIES c
JOIN TICK_STATUS t ON c.FIPS = t.FIPS
JOIN CASE_DATA cd ON c.FIPS = cd.FIPS
JOIN POPULATION p ON c.FIPS = p.FIPS
FETCH FIRST 20 ROWS ONLY;


SELECT c.FIPS, c.COUNTY_NAME, c.STATE_NAME, t.SCAPULARIS_STATUS,
       (cd.Y2020 + cd.Y2021 + cd.Y2022 + cd.Y2023) AS total_cases,
       p.POP_2023,
       ROUND(((cd.Y2020 + cd.Y2021 + cd.Y2022 + cd.Y2023) / p.POP_2023) * 100000, 2) AS case_rate_per_100k
FROM COUNTIES c
JOIN TICK_STATUS t ON c.FIPS = t.FIPS
JOIN CASE_DATA cd ON c.FIPS = cd.FIPS
JOIN POPULATION p ON c.FIPS = p.FIPS
WHERE t.SCAPULARIS_STATUS = 'Established'
ORDER BY case_rate_per_100k ASC
FETCH FIRST 20 ROWS ONLY;

-- Established tick population, but 0 case counties -- 
SELECT cd.INCIDENCE_STATUS, COUNT(*) AS zero_case_established_counties
FROM COUNTIES c
JOIN TICK_STATUS t ON c.FIPS = t.FIPS
JOIN CASE_DATA cd ON c.FIPS = cd.FIPS
WHERE t.SCAPULARIS_STATUS = 'Established'
AND (cd.Y2020 + cd.Y2021 + cd.Y2022 + cd.Y2023) = 0
GROUP BY cd.INCIDENCE_STATUS;

-- Overall established counties -- 
SELECT cd.INCIDENCE_STATUS, COUNT(*) AS total_established_counties
