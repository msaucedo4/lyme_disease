CREATE TABLE URBAN_RURAL (
    FIPS VARCHAR2(5),
    URBAN_RURAL_CODE VARCHAR2(30),
    CONSTRAINT fk_ur_county FOREIGN KEY (FIPS) REFERENCES COUNTIES(FIPS)
);

INSERT INTO URBAN_RURAL (FIPS, URBAN_RURAL_CODE)
SELECT FIPS, URBAN_RURAL_CODE
FROM URBAN_RURAL_DF
WHERE FIPS IN (SELECT FIPS FROM COUNTIES);

DROP TABLE URBAN_RURAL;

SELECT COUNT(*) FROM URBAN_RURAL;

-- 0 Case Count based on Rural/Urban Counties --
SELECT ur.URBAN_RURAL_CODE, COUNT(*) AS zero_case_count
FROM COUNTIES c
JOIN TICK_STATUS t ON c.FIPS = t.FIPS
JOIN CASE_DATA cd ON c.FIPS = cd.FIPS
JOIN URBAN_RURAL ur ON c.FIPS = ur.FIPS
WHERE t.SCAPULARIS_STATUS = 'Established'
AND (cd.Y2020 + cd.Y2021 + cd.Y2022 + cd.Y2023) = 0
GROUP BY ur.URBAN_RURAL_CODE
ORDER BY ur.URBAN_RURAL_CODE;

-- Case count based on Rural/Urban Counties -- 
SELECT ur.URBAN_RURAL_CODE, COUNT(*) AS total_established_count
FROM COUNTIES c
JOIN TICK_STATUS t ON c.FIPS = t.FIPS
JOIN URBAN_RURAL ur ON c.FIPS = ur.FIPS
WHERE t.SCAPULARIS_STATUS = 'Established'
GROUP BY ur.URBAN_RURAL_CODE
ORDER BY ur.URBAN_RURAL_CODE;

